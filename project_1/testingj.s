.globl  vbsme


# Preconditions:
#   1st parameter (a0) address of the first element of the dimension info (address of asize[0])
#   2nd parameter (a1) address of the first element of the frame array (address of frame[0][0])
#   3rd parameter (a2) address of the first element of the window array (address of window[0][0])
# Postconditions:	
#   result (v0) x coordinate of the block in the frame with the minimum SAD
#          (v1) y coordinate of the block in the frame with the minimum SAD


# Begin subroutine
#vbsme:  
    #li      $v0, 0              # reset $v0 and $V1
    #li      $v1, 0
	
    #jr $ra 

vbsme:
    # Prologue / save
    add   $v0, $zero, $zero
    add   $v1, $zero, $zero

    addi  $sp, $sp, -52
    sw    $ra,   0($sp)
    sw    $s0,   4($sp)      # i
    sw    $s1,   8($sp)      # j
    sw    $s2,  12($sp)      # frame base
    sw    $s3,  16($sp)      # win_h
    sw    $s4,  20($sp)      # win_w
    sw    $s5,  24($sp)      # cols (stride)
    sw    $s6,  28($sp)      # window base
    sw    $s7,  32($sp)      # imax

    # Copy pointers out of $a*
    add   $t6, $a0, $zero    # t6 = asize base
    add   $s2, $a1, $zero    # frame base
    add   $s6, $a2, $zero    # window base

    # Load sizes
    lw    $t0,  0($t6)       # rows
    lw    $t1,  4($t6)       # cols
    lw    $t2,  8($t6)       # win_h
    lw    $t3, 12($t6)       # win_w

    # Stash stride and window sizes
    add   $s5, $t1, $zero    # cols/stride
    add   $s3, $t2, $zero    # win_h
    add   $s4, $t3, $zero    # win_w

    # Rw = rows - win_h + 1 ; Cw = cols - win_w + 1
    sub   $t4, $t0, $t2
    addi  $t4, $t4, 1        # Rw  (KEEP UNTOUCHED)
    sub   $t5, $t1, $t3
    addi  $t5, $t5, 1        # Cw  (KEEP UNTOUCHED)

    # minSad = -1 (unset), best=(0,0)
    addi  $t7, $zero, -1
    sw    $t7, 36($sp)       # minSad
    sw    $zero, 40($sp)     # bestI
    sw    $zero, 44($sp)     # bestJ

    # s = 0 ; Smax = Rw + Cw - 2  (store on stack at 48($sp))
    add   $t7, $zero, $zero  # s
    add   $t8, $t4, $t5
    addi  $t8, $t8, -2
    sw    $t8, 48($sp)       # Smax saved; inner loops can use $t8 freely

# ===== diagonal loop over s =====
VB_diag:
    # if (s > Smax) break
    lw    $t8, 48($sp)       # reload Smax
    slt   $t9, $t8, $t7
    bne   $t9, $zero, VB_done

    # imin = max(0, s-(Cw-1)) -> t0
    add   $t0, $t5, $zero
    addi  $t0, $t0, -1       # t0 = Cw-1
    sub   $t0, $t7, $t0      # t0 = s-(Cw-1)
    slt   $t9, $t0, $zero
    beq   $t9, $zero, VB_imin_ok
    add   $t0, $zero, $zero  # t0 = imin = 0
VB_imin_ok:

    # imax = min(Rw-1, s) -> s7
    add   $t1, $t4, $zero
    addi  $t1, $t1, -1       # t1 = Rw-1
    slt   $t9, $t1, $t7
    bne   $t9, $zero, VB_imax_rw1
    add   $s7, $t7, $zero    # imax = s
    j     VB_imax_ok
VB_imax_rw1:
    add   $s7, $t1, $zero    # imax = Rw-1
VB_imax_ok:

    # even/odd s ?
    andi  $t1, $t7, 1
    bne   $t1, $zero, VB_odd

# ---------- EVEN s: i asc from imin -> imax ----------
    add   $s0, $t0, $zero    # i = imin
VB_even_i:
    # if (i > imax) next diagonal
    slt   $t9, $s7, $s0
    bne   $t9, $zero, VB_next_s

    # j = s - i
    sub   $s1, $t7, $s0

    # guard 0<=i<Rw and 0<=j<Cw
    slt   $t9, $s0, $zero
    bne   $t9, $zero, VB_even_next_i
    slt   $t9, $t4, $s0
    bne   $t9, $zero, VB_even_next_i
    slt   $t9, $s1, $zero
    bne   $t9, $zero, VB_even_next_i
    slt   $t9, $t5, $s1
    bne   $t9, $zero, VB_even_next_i

    # ----- SAD(i,j) -----
    add   $t2, $zero, $zero  # SAD = 0
    add   $t3, $zero, $zero  # r = 0
VB_sad_row_e:
    slt   $t9, $t3, $s3
    beq   $t9, $zero, VB_sad_done_e

    add   $t6, $zero, $zero  # c = 0
VB_sad_col_e:
    slt   $t9, $t6, $s4
    beq   $t9, $zero, VB_sad_nextrow_e

    # frame[(i+r, j+c)]
    add   $t1, $s0, $t3          # i+r
    mul   $t1, $t1, $s5          # (i+r)*cols
    add   $t1, $t1, $s1          # + j
    add   $t1, $t1, $t6          # + c
    sll   $t1, $t1, 2            # *4
    add   $t1, $t1, $s2          # + frame base
    lw    $t0, 0($t1)            # frame value

    # window[(r,c)]
    mul   $t1, $t3, $s4          # r*win_w
    add   $t1, $t1, $t6          # + c
    sll   $t1, $t1, 2
    add   $t1, $t1, $s6
    lw    $t8, 0($t1)            # window value

    # |frame - window|
    sub   $t1, $t0, $t8
    bgez  $t1, VB_abs_ok_e
    sub   $t1, $zero, $t1
VB_abs_ok_e:
    add   $t2, $t2, $t1          # SAD += diff

    addi  $t6, $t6, 1            # ++c
    j     VB_sad_col_e

VB_sad_nextrow_e:
    addi  $t3, $t3, 1            # ++r
    j     VB_sad_row_e

VB_sad_done_e:
    # last-on-tie (<=)
    lw    $t1, 36($sp)           # minSad
    bltz  $t1, VB_setmin_e
    slt   $t9, $t2, $t1
    bne   $t9, $zero, VB_setmin_e
    beq   $t2, $t1, VB_setmin_e
    j     VB_keepmin_e

VB_setmin_e:
    sw    $t2, 36($sp)
    sw    $s0, 40($sp)
    sw    $s1, 44($sp)
VB_keepmin_e:

VB_even_next_i:
    addi  $s0, $s0, 1            # i++
    j     VB_even_i

# ---------- ODD s: i desc from imax -> imin ----------
VB_odd:
    add   $s0, $s7, $zero        # i = imax
VB_odd_i:
    # if (i < imin) next diagonal
    slt   $t9, $s0, $t0
    bne   $t9, $zero, VB_next_s

    # j = s - i
    sub   $s1, $t7, $s0

    # guard 0<=i<Rw and 0<=j<Cw
    slt   $t9, $s0, $zero
    bne   $t9, $zero, VB_odd_next_i
    slt   $t9, $t4, $s0
    bne   $t9, $zero, VB_odd_next_i
    slt   $t9, $s1, $zero
    bne   $t9, $zero, VB_odd_next_i
    slt   $t9, $t5, $s1
    bne   $t9, $zero, VB_odd_next_i

    # ----- SAD(i,j) -----
    add   $t2, $zero, $zero      # SAD = 0
    add   $t3, $zero, $zero      # r = 0
VB_sad_row_o:
    slt   $t9, $t3, $s3
    beq   $t9, $zero, VB_sad_done_o

    add   $t6, $zero, $zero      # c = 0
VB_sad_col_o:
    slt   $t9, $t6, $s4
    beq   $t9, $zero, VB_sad_nextrow_o

    # frame[(i+r, j+c)]
    add   $t1, $s0, $t3
    mul   $t1, $t1, $s5
    add   $t1, $t1, $s1
    add   $t1, $t1, $t6
    sll   $t1, $t1, 2
    add   $t1, $t1, $s2
    lw    $t0, 0($t1)            # frame value

    # window[(r,c)]
    mul   $t1, $t3, $s4
    add   $t1, $t1, $t6
    sll   $t1, $t1, 2
    add   $t1, $t1, $s6
    lw    $t8, 0($t1)            # window value

    # |frame - window|
    sub   $t1, $t0, $t8
    bgez  $t1, VB_abs_ok_o
    sub   $t1, $zero, $t1
VB_abs_ok_o:
    add   $t2, $t2, $t1

    addi  $t6, $t6, 1
    j     VB_sad_col_o

VB_sad_nextrow_o:
    addi  $t3, $t3, 1
    j     VB_sad_row_o

VB_sad_done_o:
    # last-on-tie (<=)
    lw    $t1, 36($sp)
    bltz  $t1, VB_setmin_o
    slt   $t9, $t2, $t1
    bne   $t9, $zero, VB_setmin_o
    beq   $t2, $t1, VB_setmin_o
    j     VB_keepmin_o

VB_setmin_o:
    sw    $t2, 36($sp)
    sw    $s0, 40($sp)
    sw    $s1, 44($sp)
VB_keepmin_o:

VB_odd_next_i:
    addi  $s0, $s0, -1           # i--
    j     VB_odd_i

# ----- advance diagonal s -----
VB_next_s:
    addi  $t7, $t7, 1
    j     VB_diag

# ===== return bestI/bestJ =====
VB_done:
    lw    $v0, 40($sp)
    lw    $v1, 44($sp)

    # Epilogue / restore
    lw    $ra,   0($sp)
    lw    $s0,   4($sp)
    lw    $s1,   8($sp)
    lw    $s2,  12($sp)
    lw    $s3,  16($sp)
    lw    $s4,  20($sp)
    lw    $s5,  24($sp)
    lw    $s6,  28($sp)
    lw    $s7,  32($sp)
    addi  $sp, $sp, 52
    jr    $ra
